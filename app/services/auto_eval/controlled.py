"""Design 2 — controlled (shared-context) comparison.

One FA "preparer" runs the full pre-computation once (route → tighten → /llm_retrieve →
gate → build prompt) via `prepare_only`, then the IDENTICAL prompt is generated by Qwen and
EuroLLM through each model's raw vLLM /v1 endpoint with identical params. The only variable
between those two is the model. Mistral stays a full-system external entrant (its own RAG),
flagged `controlled_context=false`.
"""
from __future__ import annotations

import asyncio
import json
import time
from typing import Any
from urllib.parse import urlencode

import httpx

from app.config import get_settings

from .fanout import (
    _auth_headers,
    _experiment_headers,
    _extract_sse_events,
    _mistral_answer,
    _token_usage,
    _variant_metadata,
)
from .models import LocalizedQuestion, VariantAnswer

S = get_settings()


def _preparer_url() -> str:
    return (getattr(S, "CONTROLLED_PREPARER_URL", "") or getattr(S, "FARM_ASSISTANT_UM_QWEN3_URL", "")).rstrip("/")


async def prepare_shared_context(
    question: LocalizedQuestion, access_token: str, refresh_token: str | None, batch_id: str,
) -> dict[str, Any]:
    """Run prepare_only on the preparer FA and return the assembled prompt + sources."""
    base = _preparer_url()
    headers = {
        **_auth_headers(access_token, refresh_token),
        **_experiment_headers(batch_id, question, {"id": "controlled_preparer", "backend": "preparer"}),
    }
    query = urlencode({"q": question.question, "pause_personalization": "true", "prepare_only": "true"})
    timeout = httpx.Timeout(getattr(S, "AUTOMATION_REQUEST_TIMEOUT", 180.0))
    async with httpx.AsyncClient(timeout=timeout, verify=S.VERIFY_SSL, trust_env=False) as client:
        resp = await client.get(f"{base}/chatbot/api/chats/message/stream?{query}", headers=headers)
        resp.raise_for_status()
        text = resp.text
    for event, raw in _extract_sse_events(text):
        if event == "prompt":
            return json.loads(raw)
        if event == "app_error":
            raise RuntimeError(f"preparer error: {raw[:300]}")
    raise RuntimeError("preparer returned no prompt event (is prepare_only deployed?)")


async def _generate_raw_vllm(
    client: httpx.AsyncClient, base_url: str, model: str, api_key: str,
    messages: list[dict[str, str]], temperature: float, max_tokens: int | None, top_p: float,
) -> tuple[str, dict[str, Any]]:
    payload: dict[str, Any] = {"model": model, "messages": messages, "temperature": temperature, "top_p": top_p}
    if isinstance(max_tokens, int) and max_tokens > 0:
        payload["max_tokens"] = max_tokens
    payload["stream"] = False
    headers = {"Content-Type": "application/json"}
    if api_key:
        headers["Authorization"] = f"Bearer {api_key}"
    resp = await client.post(f"{base_url.rstrip('/')}/v1/chat/completions", headers=headers, json=payload)
    resp.raise_for_status()
    data = resp.json()
    content = ((data.get("choices") or [{}])[0].get("message") or {}).get("content") or ""
    return content.strip(), (data.get("usage") or {})


def _controlled_metadata(variant_id: str, backend: str, label: str) -> dict[str, Any]:
    meta = _variant_metadata({"id": variant_id, "backend": backend, "label": label})
    meta["controlled_context"] = True
    meta["generation_transport"] = "raw_vllm"
    return meta


async def answer_question_controlled(
    question: LocalizedQuestion, access_token: str, refresh_token: str | None, batch_id: str,
) -> list[VariantAnswer]:
    # 1) One shared prepare. If it fails, the two controlled models can't generate, but
    #    Mistral (independent) is still attempted.
    messages: list[dict[str, str]] | None = None
    sources: list[dict[str, Any]] = []
    grounding: str | None = None
    temperature: float = getattr(S, "TEMPERATURE", 0.4)
    max_tokens: int | None = None
    prepare_error: str | None = None
    try:
        prepared = await prepare_shared_context(question, access_token, refresh_token, batch_id)
        messages = prepared.get("messages") or []
        sources = prepared.get("sources") or []
        grounding = prepared.get("grounding_mode")
        temperature = prepared.get("temperature", temperature)
        max_tokens = prepared.get("max_tokens")
    except Exception as error:
        prepare_error = str(error)[:1000]

    top_p = getattr(S, "CONTROLLED_TOP_P", 0.9)
    qwen = (
        getattr(S, "CONTROLLED_QWEN_VLLM_URL", "") or S.VLLM_URL,
        getattr(S, "CONTROLLED_QWEN_MODEL", "") or S.VLLM_MODEL,
        getattr(S, "CONTROLLED_QWEN_API_KEY", "") or (S.VLLM_API_KEY or ""),
    )
    eurollm = (
        getattr(S, "CONTROLLED_EUROLLM_VLLM_URL", ""),
        getattr(S, "CONTROLLED_EUROLLM_MODEL", "") or "current",
        getattr(S, "CONTROLLED_EUROLLM_API_KEY", ""),
    )
    timeout = httpx.Timeout(getattr(S, "AUTOMATION_REQUEST_TIMEOUT", 180.0))

    async with httpx.AsyncClient(timeout=timeout, verify=S.VERIFY_SSL, trust_env=False) as client:
        async def controlled_answer(variant_id: str, backend: str, label: str, cfg: tuple[str, str, str]) -> VariantAnswer:
            base_url, model, api_key = cfg
            started = time.monotonic()
            if not messages:
                return VariantAnswer(
                    variant_id=variant_id, backend=backend,
                    assistant_message="This response could not be generated.",
                    error=prepare_error or "shared context unavailable",
                    variant_metadata=_controlled_metadata(variant_id, backend, label),
                    runtime_metadata={"controlled_context": True, "stream_completed": False, "error": prepare_error},
                )
            try:
                text, usage = await _generate_raw_vllm(client, base_url, model, api_key, messages, temperature, max_tokens, top_p)
                if not text:
                    raise RuntimeError("model returned an empty response")
                return VariantAnswer(
                    variant_id=variant_id, backend=backend, assistant_message=text,
                    latency_ms=int((time.monotonic() - started) * 1000),
                    sources=sources, grounding_mode=grounding,
                    variant_metadata=_controlled_metadata(variant_id, backend, label),
                    runtime_metadata={
                        "controlled_context": True,
                        "shared_grounding_mode": grounding,
                        "request_endpoint": "/v1/chat/completions",
                        "session_mode": "controlled_raw_vllm",
                        "gen_model": model,
                        "answer_length_chars": len(text),
                        "sources_count": len(sources),
                        "token_usage": _token_usage(text, usage),
                        "stream_completed": True,
                    },
                )
            except Exception as error:
                return VariantAnswer(
                    variant_id=variant_id, backend=backend,
                    assistant_message="This response could not be generated.",
                    latency_ms=int((time.monotonic() - started) * 1000), error=str(error)[:1000],
                    variant_metadata=_controlled_metadata(variant_id, backend, label),
                    runtime_metadata={"controlled_context": True, "stream_completed": False, "error": str(error)[:500]},
                )

        results = await asyncio.gather(
            controlled_answer("qwen3", "um_qwen3", "Qwen3", qwen),
            controlled_answer("eurollm", "eurollm", "EuroLLM", eurollm),
            _mistral_answer(client, question, {"id": "mistral", "backend": "euf_chatbot_tnods", "label": "Mistral"}),
        )

    # Mistral is a full-system entrant: it does its own retrieval, so flag it as not sharing
    # the controlled context (its grounding/sources are its own).
    for answer in results:
        if answer.backend == "euf_chatbot_tnods":
            answer.variant_metadata["controlled_context"] = False
            answer.runtime_metadata["controlled_context"] = False
    return list(results)
